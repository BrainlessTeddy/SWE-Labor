package com.artcreator.model;

/**
 * Cross-sectional shape of a single stick on the printed template.
 *
 * <p>The shape is purely a visualisation choice for the printed marker; it
 * does not change the colour or position of the stick.</p>
 */
public enum StickShape {
    CIRCLE,
    SQUARE,
    HEXAGON,
    TRIANGLE
}
